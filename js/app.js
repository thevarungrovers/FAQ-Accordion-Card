// 1
function show1(){
    if(document.getElementById('ans1').style.display === 'none'){
        // down
        document.getElementById('up1').style.transform = 'rotate(180deg)';
        
        // show answer
        document.getElementById('ans1').style.display = 'block';
    }

    else{
        // up
        document.getElementById('up1').style.transform = 'rotate(0deg)';

        // hide answer
        document.getElementById('ans1').style.display = 'none';
    }
}

